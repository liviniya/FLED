public class ImageFuzzyNumber {
    private final double white, black;

    public ImageFuzzyNumber(double white, double black) {
        this.white = white;
        this.black = black;
    }

    public double getWhite() {
        return white;
    }

    public double getBlack() {
        return black;
    }
}
